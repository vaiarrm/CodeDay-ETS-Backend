package Driver;

public class DriverInfo {
	private String driverFirstName;
	private String driverLastName;
	private String driverPhoneNumber;
	private String driverStatus;
	private String driverLicenseDetails;
	public String getDriverFirstName() {
		return driverFirstName;
	}
	public void setDriverFirstName(String driverFirstName) {
		this.driverFirstName = driverFirstName;
	}
	public String getDriverLastName() {
		return driverLastName;
	}
	public void setDriverLastName(String driverLastName) {
		this.driverLastName = driverLastName;
	}
	public String getDriverPhoneNumber() {
		return driverPhoneNumber;
	}
	public void setDriverPhoneNumber(String driverPhoneNumber) {
		this.driverPhoneNumber = driverPhoneNumber;
	}
	public String getDriverStatus() {
		return driverStatus;
	}
	public void setDriverStatus(String driverStatus) {
		this.driverStatus = driverStatus;
	}
	public String getDriverLicenseDetails() {
		return driverLicenseDetails;
	}
	public void setDriverLicenseDetails(String driverLicenseDetails) {
		this.driverLicenseDetails = driverLicenseDetails;
	}
	
	

}
